struct Node {
    std::string value;
    std::string lexeme;
    std::vector<std::shared_ptr<Node>> children;
    std::string type_annotation;
    Node(std::string value, std::string lexeme) : value(value), lexeme(lexeme) {}
    Node(std::string value) : value(value) {}
};

struct Rule {
    std::string lhs;
    std::vector<std::string> rhs;
};

